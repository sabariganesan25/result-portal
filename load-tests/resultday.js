/**
 * load-tests/resultday.js
 * ─────────────────────────────────────────────────────────────
 * RESULT DAY SIMULATION — 100 students logging in simultaneously.
 * Ramps up to simulate a massive spike.
 */

import http from 'k6/http';
import { check, sleep, group } from 'k6';
import { Counter, Rate, Trend } from 'k6/metrics';
import { SharedArray } from 'k6/data';

// Custom metrics
const loginSuccessCount = new Counter('result_day_login_success');
const loginFailureCount = new Counter('result_day_login_failure');
const loginErrorRate    = new Rate('result_day_login_error_rate');
const loginDuration     = new Trend('result_day_login_duration_ms', true);

// Load student credentials generated by seed script
const students = new SharedArray('students', function () {
  try {
    // Try to load the generated credentials
    return JSON.parse(open('../scripts/student_credentials.json'));
  } catch (e) {
    console.warn('Could not load student_credentials.json, using fallback');
    return [{ registration_no: '711521104001', password: 'arjun001' }];
  }
});

export const options = {
  scenarios: {
    // Phase 1: Normal background traffic
    normal_traffic: {
      executor: 'constant-vus',
      vus: 5,
      duration: '30s',
      startTime: '0s',
      exec: 'normalTraffic',
    },
    // Phase 2: The MASSIVE Spike
    result_day_spike: {
      executor: 'ramping-vus',
      startVUs: 0,
      stages: [
        { duration: '30s', target: 200 },  // Fast ramp
        { duration: '1m',  target: 500 },  // Sustained pressure
        { duration: '1m',  target: 1000 }, // Full overload
        { duration: '2m',  target: 1000 }, // Hold overload
        { duration: '30s', target: 0   },  // Cool down
      ],
      startTime: '30s',
      exec: 'loginAndFetchResults',
    },
  },
  thresholds: {
    http_req_duration: ['p(95)<10000'], // Allow up to 10s under heavy load
    'result_day_login_error_rate': ['rate<0.5'], // Allow up to 50% errors for this simulation
  },
};

const BASE_URL = __ENV.BASE_URL || 'http://localhost:5000';
const HEADERS = { 'Content-Type': 'application/json' };

export function normalTraffic() {
  const res = http.get(`${BASE_URL}/api/health`);
  check(res, { 'health check ok': (r) => r.status === 200 });
  sleep(1);
}

export function loginAndFetchResults() {
  const student = students[Math.floor(Math.random() * students.length)];

  group('Aggressive Result Day Flow', function () {
    const loginStart = Date.now();
    
    // 1. Login (High CPU/DB cost)
    const loginRes = http.post(
      `${BASE_URL}/api/login`,
      JSON.stringify(student),
      { headers: HEADERS, tags: { name: 'login' } }
    );

    const loginTime = Date.now() - loginStart;
    loginDuration.add(loginTime);

    const loginOk = check(loginRes, {
      'login successful': (r) => r.status === 200,
    });

    if (loginOk) {
      loginSuccessCount.add(1);
      loginErrorRate.add(false);

      const token = loginRes.json().token;
      const authHeader = Object.assign({}, HEADERS, { Authorization: `Bearer ${token}` });
      
      // 2. HEAVY FETCHING (Simulate student refreshing and checking profile)
      for (let i = 0; i < 5; i++) {
        // Fetch Results
        http.get(
          `${BASE_URL}/api/results/${student.registration_no}`,
          { headers: authHeader, tags: { name: 'fetch_results' } }
        );

        // Fetch Profile
        http.get(
          `${BASE_URL}/api/student/${student.registration_no}`,
          { headers: authHeader, tags: { name: 'fetch_profile' } }
        );

        // Very small delay to keep pressure high but allow some socket reuse
        sleep(0.1); 
      }

    } else {
      loginFailureCount.add(1);
      loginErrorRate.add(true);
    }

    // Almost no think time for "Panicked Student" behavior
    sleep(0.2); 
  });
}

